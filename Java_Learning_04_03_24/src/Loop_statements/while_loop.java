package Loop_statements;

public class while_loop {

	public static void main(String[] args) {
		// While statement
		//print all natural  numbers till 10 
		int i =1;
		
		while(i<=10) {
			
			System.out.println(i);
			i++;//Increment operator i=i+1
		}
             System.out.println("program exited");
	}

}
